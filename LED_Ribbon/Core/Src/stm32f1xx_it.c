void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
    static uint16_t Tempnum = 0;
    static uint8_t count = 0;
    static uint16_t Tick = 0;
    //TIM2每10ms进一次中断
    if(htim->Instance == TIM2) //判断TIM2的更新中�???????????????????????????????
    {
        if(Uart2_Package.RxStartFalg == 1) //若串口开始接收数�????????????????????????????
        {
            if(Tempnum == Uart2_Package.Cur_RxLength ) //说明串口结束了接收数�????????????????????????
            {
                Uart2_Package.RxEndFalg = 1;
                Uart2_Package.RxStartFalg = 0;
            }
            else
            {
                Tempnum = Uart2_Package.Cur_RxLength;
            }
        }
    }
    //TIM3每1ms进一次中断
    else if(htim->Instance == TIM3)
    {
        Tick++;
        if(Tick>=hLedRib.RandomLED.SwitchPeriod)
        {
            hLedRib.RandomLED.StartFlag = 1;
            Tick = 0;
        }
        if(hLedRib.RandomLED.StartFlag == 0 && (Tick%10 == 0))
        {
            if(count>=60)count = 0;
            hLedRib.RandomLED.RandomBuff[count++] = (HAL_GetTick()*HAL_GetTick())%256; //这里搞个相乘就只是为了让连续两次得到的数据不那么连贯
        }
    }
}

